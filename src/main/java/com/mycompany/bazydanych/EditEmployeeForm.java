    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int rowIndex = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        jTextField_id.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField_fname.setText(model.getValueAt(rowIndex, 1).toString());
        jTextField_lname.setText(model.getValueAt(rowIndex, 2).toString());
        jTextField_email.setText(model.getValueAt(rowIndex, 3).toString());
        jTextField_phone.setText(model.getValueAt(rowIndex, 4).toString());
        Date hire_date;
        try {
            hire_date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 5).toString());
            jDateChooser_hire_date.setDate(hire_date);
        } catch (ParseException ex) {
            Logger.getLogger(EditEmployeeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextField_job_id.setText(model.getValueAt(rowIndex, 6).toString());
        jTextField_salary.setText(model.getValueAt(rowIndex, 7).toString());
        if(model.getValueAt(rowIndex, 8)!="null"){
        jTextField_comm_pct.setText(model.getValueAt(rowIndex, 8).toString());}
        else jTextField_comm_pct.setText("");
        if(model.getValueAt(rowIndex, 9)!="null"){
        jTextField_manager.setText(model.getValueAt(rowIndex, 9).toString());}
        else jTextField_manager.setText("");
        if(model.getValueAt(rowIndex, 10)!="null"){
        jTextField_department.setText(model.getValueAt(rowIndex, 10).toString());}
        else jTextField_department.setText("");
        
    }//GEN-LAST:event_jTable1MouseClicked

